package io.github.omegabird113.rollingforever;

import com.badlogic.gdx.ApplicationAdapter;
import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.PerspectiveCamera;
import com.badlogic.gdx.graphics.g3d.Environment;
import com.badlogic.gdx.graphics.g3d.ModelBatch;
import com.badlogic.gdx.graphics.g3d.attributes.ColorAttribute;
import com.badlogic.gdx.graphics.g3d.environment.DirectionalLight;
import com.badlogic.gdx.graphics.g3d.utils.ModelBuilder;
import io.github.omegabird113.rollingforever.utils.CameraFollowUtils;

public class Main extends ApplicationAdapter {
    private final InstanceManager instanceManager = new InstanceManager();
    private PerspectiveCamera camera;
    private Environment environment;
    private ModelBatch modelBatch;

    @Override
    public void create() {
        camera = new PerspectiveCamera(67, Gdx.graphics.getWidth(), Gdx.graphics.getHeight());
        camera.position.set(CameraFollowUtils.getRelativePosition());
        camera.lookAt(0f, 0f, 0f);
        camera.near = 1f;
        camera.far = 300f;
        camera.update();

        environment = new Environment();
        environment.set(new ColorAttribute(ColorAttribute.AmbientLight, 0.6f, 0.6f, 0.6f, 1f));
        environment.add(new DirectionalLight().set(0.8f, 0.8f, 0.8f, -1f, -0.8f, -0.2f));

        ModelBuilder modelBuilder = new ModelBuilder();
        instanceManager.createInitial(modelBuilder);
        modelBatch = new ModelBatch();
    }

    @Override
    public void render() {
        float delta = Gdx.graphics.getDeltaTime();
        instanceManager.render(delta, modelBatch, camera, environment);
    }

    @Override
    public void resize(int width, int height) {
        camera.viewportWidth = width;
        camera.viewportHeight = height;
        camera.update();
    }

    @Override
    public void dispose() {
        modelBatch.dispose();
        instanceManager.disposeAll();
    }
}
